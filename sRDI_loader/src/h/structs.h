/**
 * @file structs.h
 * @brief Contains definitions of various structures and enumerations used in Windows programming.
 *
 * This header file defines several structures and enumerations commonly used in Windows API
 * programming, including data types for file information, object attributes, I/O status blocks,
 * and relocation blocks.
 *
 * Structures:
 * - BASE_RELOCATION_BLOCK: Represents a base relocation block with page address and block size.
 * - BASE_RELOCATION_ENTRY: Represents an entry in a base relocation block with offset and type.
 * - UNICODE_STRING: Represents a Unicode string with length, maximum length, and buffer pointer.
 * - OBJECT_ATTRIBUTES: Represents attributes of an object, including name, security, and root directory.
 * - IO_STATUS_BLOCK: Represents the status of an I/O operation, including status and information.
 * - FILE_STANDARD_INFORMATION: Contains standard file information such as size, links, and type.
 * - ANSI_STRING: Represents an ANSI string with length, maximum length, and buffer pointer.
 *
 * Enumerations:
 * - FILE_INFORMATION_CLASS: Enumerates various classes of file information, such as directory,
 *   basic, standard, and extended information.
 *
 * Dependencies:
 * - <windows.h>: Required for Windows-specific data types and definitions.
 */
